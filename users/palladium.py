# Кириллические гласные, с которых может начинаться второй слог-палладий.
# (Заглавные тоже добавляем, хотя в нашем потоке всегда строчные.)
_PLD_VOWELS = set('аоэыиуюяеёАОЭЫИУЮЯЕЁ')


class UnknownPinyin(KeyError):
    """Бросаем при попытке транскрибировать несуществующий слог."""


def to_palladium(pinyin):
    """Палладий-запись одиночного слога пиньиня без тонов."""
    s = (pinyin or '').strip().lower()
    if s not in PALLADIUS:
        raise UnknownPinyin(f'Неизвестный слог пиньиня: {pinyin!r}')
    return PALLADIUS[s]


def starts_with_vowel(palladium_syllable):
    """True, если палладий-слог начинается с гласной."""
    return bool(palladium_syllable) and palladium_syllable[0] in _PLD_VOWELS


def join_palladium(pinyin1, pinyin2):
    """Склеить два слога пиньиня в одно двусложное слово по правилам Палладия.

    >>> join_palladium('chang', 'an')
    'чанъань'
    >>> join_palladium('tian', 'an')
    'тяньань'
    >>> join_palladium('xi', 'an')
    'сиань'
    >>> join_palladium('xie', 'zhang')
    'сечжан'
    >>> join_palladium('peng', 'you')
    'пэнъю'
    """
    s1 = (pinyin1 or '').strip().lower()
    s2 = (pinyin2 or '').strip().lower()
    r1 = to_palladium(s1)
    r2 = to_palladium(s2)

    # Главное правило: -ng + гласная начала второго слога → разделитель 'ъ'.
    if s1.endswith('ng') and starts_with_vowel(r2):
        return r1 + 'ъ' + r2
    return r1 + r2


