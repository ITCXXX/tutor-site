def generate_task():
    """ОГЭ №19. Случайный вариант:
      - multi: 2 истинных + 1 ложное (ответ — 2 цифры);
      - single: 1 истинное + 2 ложных (ответ — 1 цифра).
    """
    variant = random.choice(["multi", "single"])
    if variant == "multi":
        chosen_t = random.sample(TRUES, 2)
        chosen_f = random.sample(FALSES, 1)
        stmts = chosen_t + chosen_f
        random.shuffle(stmts)
        ans_nums = sorted(i + 1 for i, s in enumerate(stmts) if s in chosen_t)
        answer = "".join(str(x) for x in ans_nums)
        intro = ("<b>Выберите один или несколько правильных ответов.</b><br>"
                 "Какие из следующих утверждений являются истинными высказываниями?")
        outro = ("В ответ запишите <b>номера истинных высказываний</b> без пробелов, "
                 "запятых и других дополнительных символов.")
    else:
        chosen_f = random.sample(FALSES, 2)
        chosen_t = random.sample(TRUES, 1)
        stmts = chosen_t + chosen_f
        random.shuffle(stmts)
        ans_num = next(i + 1 for i, s in enumerate(stmts) if s in chosen_t)
        answer = str(ans_num)
        intro = ("<b>Выберите правильный ответ.</b><br>"
                 "Какое из следующих утверждений является истинным высказыванием?")
        outro = "В ответ запишите <b>номер истинного высказывания</b>."

    body_lines = [intro, "<br>"]
    for i, s in enumerate(stmts, 1):
        body_lines.append(f'{i}) {s}<br>')
    body_lines.append("<br>" + outro)
    body = "\n".join(body_lines)
    return {"condition_text": body, "correct_answer": answer}
