class Command(BaseCommand):
    help = "Populate EGE Task 9 questions (прикладные задачи с формулами)"

    def add_arguments(self, parser):
        parser.add_argument(
            "--clear", action="store_true",
            help="Delete existing EGE-9 lesson before inserting",
        )

    def handle(self, *args, **options):
        course = Course.objects.filter(slug="ege-profile-math").first()
        if not course:
            self.stdout.write(self.style.ERROR("Course 'ege-profile-math' not found."))
            return

        module, _ = Module.objects.get_or_create(
            course=course, order=1,
            defaults={"title": "Первая часть", "description": "Задачи первой части профильного ЕГЭ."},
        )

        if options["clear"]:
            deleted, _ = Lesson.objects.filter(module=module, order=9).delete()
            self.stdout.write(self.style.WARNING(f"Deleted lesson order=9: {deleted} objects"))

        lesson, created = Lesson.objects.get_or_create(
            module=module, order=9,
            defaults={"title": "Задание 9", "lesson_type": "practice"},
        )
        if not created:
            self.stdout.write(self.style.WARNING("Lesson exists (use --clear to reset)."))

        total_q = 0
        for data in ASSIGNMENTS:
            assignment, created = Assignment.objects.get_or_create(
                lesson=lesson, order=data["order"],
                defaults={
                    "title": data["title"],
                    "description": data["description"],
                    "answer_type": "decimal_input",
                    "required_correct": data["required_correct"],
                },
            )
            if not created:
                self.stdout.write(self.style.WARNING(
                    f"  Assignment exists: {assignment.title} -- skipping"
                ))
                continue

            for idx, item in enumerate(data["questions"]):
                if len(item) == 3:
                    q_text, answer, image_svg = item
                else:
                    q_text, answer = item
                    image_svg = ""
                question = TestQuestion.objects.create(
                    assignment=assignment,
                    question_text=q_text,
                    order=idx + 1,
                    image_svg=image_svg,
                )
                AnswerOption.objects.create(question=question, text=answer, is_correct=True)
                total_q += 1

        self.stdout.write(self.style.SUCCESS(f"Done! Lesson: Zadacha 9, {total_q} questions added."))
