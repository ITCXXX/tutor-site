class Command(BaseCommand):
    help = "Populate EGE Task 10 questions (52 items across 6 types)"

    def add_arguments(self, parser):
        parser.add_argument(
            "--clear",
            action="store_true",
            help="Delete existing EGE-10 lesson before inserting",
        )

    def handle(self, *args, **options):
        course = Course.objects.filter(slug="ege-profile-math").first()
        if not course:
            self.stdout.write(self.style.ERROR(
                "Course 'ege-profile-math' not found. Run populate_ege12 first."
            ))
            return

        module, _ = Module.objects.get_or_create(
            course=course,
            order=1,
            defaults={
                "title": "Pervaya chast",
                "description": "Zadachi pervoy chasti profil'nogo EGE.",
            },
        )

        if options["clear"]:
            deleted, _ = Lesson.objects.filter(module=module, order=10).delete()
            self.stdout.write(self.style.WARNING(f"Deleted existing lesson (order=10): {deleted} objects"))

        lesson, created = Lesson.objects.get_or_create(
            module=module,
            order=10,
            defaults={
                "title": "Задание 10",
                "lesson_type": "practice",
            },
        )
        if not created:
            self.stdout.write(self.style.WARNING(
                "Lesson already exists (use --clear to reset)."
            ))

        total_q = 0
        for lesson_data in ASSIGNMENTS:
            assignment, created = Assignment.objects.get_or_create(
                lesson=lesson,
                order=lesson_data["order"],
                defaults={
                    "title": lesson_data["title"],
                    "description": lesson_data["description"],
                    "answer_type": "decimal_input",
                    "required_correct": 5,
                },
            )
            if not created:
                self.stdout.write(self.style.WARNING(
                    f"  Assignment already exists: {assignment.title} -- skipping"
                ))
                continue

            for idx_q, (q_text, answer) in enumerate(lesson_data["questions"]):
                question = TestQuestion.objects.create(
                    assignment=assignment,
                    question_text=q_text,
                    order=idx_q + 1,
                )
                AnswerOption.objects.create(
                    question=question,
                    text=answer,
                    is_correct=True,
                    order=1,
                )
                total_q += 1

            self.stdout.write(self.style.SUCCESS(
                f"  OK {lesson_data['title']}: {len(lesson_data['questions'])} q"
            ))

        self.stdout.write(self.style.SUCCESS(
            f"\nGotovo! Urok: Zadacha 10, {total_q} voprosov."
        ))
